def _ensure_core_exists(core: str, env: dict[str, str] | None = None) -> None:
    reg = _read_registry(env)
    if not any(c.name == core for c in reg.cores):
        raise _LabNotFound(f"no core registered with short id: {core!r}")


def create_request(
    *,
    core: str,
    service: str,
    requester: str,
    requester_lab: str,
    booked_slot: BookingSlot | None = None,
    training_verified: dict[str, str] | None = None,
    prerequisites_attested: list[dict[str, bool]] | None = None,
    fee_at_booking: FeeSnapshot | None = None,
    notes: str = "",
    today: _dt.date | None = None,
    env: dict[str, str] | None = None,
) -> RequestSummary:
    """Create a new request file. State starts at ``scheduled`` if a
    ``booked_slot`` was provided, otherwise ``requested``.

    The booking endpoint (Phase 3b) wraps semantic checks around this
    primitive (training prereq, fee quote, member-active gate).
    """
    _ensure_core_exists(core, env)
    rid = new_request_id(
        core=core, requester=requester, service=service, today=today, env=env,
    )
    now_iso = _dt.datetime.now(_dt.timezone.utc).replace(microsecond=0).isoformat().replace("+00:00", "Z")
    slot = booked_slot or BookingSlot()
    state = STATE_SCHEDULED if (slot.start and slot.end) else STATE_REQUESTED
    req = RequestSummary(
        request_id=rid,
        service=service,
        core=core,
        requester=requester if requester.startswith("@") else f"@{requester}",
        requester_lab=requester_lab,
        state=state,
        booked_slot=slot,
        training_verified=dict(training_verified or {}),
        prerequisites_attested=list(prerequisites_attested or []),
        fee_at_booking=fee_at_booking or FeeSnapshot(),
        notes=notes,
        job_id=rid,   # job_id == request_id for the data-delivery layout
        created=now_iso,
        updated=now_iso,
    )
    rdir = requests_dir(core, env)
    rdir.mkdir(parents=True, exist_ok=True)
    target = rdir / f"{rid}.md"
    target.write_text(_render_request_md(req, body=notes), encoding="utf-8")
    req.path = target
    root = lab_info_root(env)
    _git_init_if_needed(root)
    _git_commit_all(root, f"core {core}: request {rid} created ({state})")
    return req


def transition_request(
    *,
    core: str,
    request_id: str,
    to_state: str,
    note: str = "",
    env: dict[str, str] | None = None,
) -> RequestSummary:
    """Advance through the state machine. Refuses illegal transitions."""
    if to_state not in VALID_STATES:
        raise RequestError(f"unknown state: {to_state!r}")
    req = get_request(core, request_id, env=env)
    if req is None:
        raise RequestError(f"request not found: {core}/{request_id}")
    allowed = ALLOWED_TRANSITIONS.get(req.state, set())
    if to_state not in allowed:
        raise RequestError(
            f"illegal transition: {req.state!r} -> {to_state!r} "
            f"(allowed: {sorted(allowed) or 'terminal'})"
        )
    now_iso = _dt.datetime.now(_dt.timezone.utc).replace(microsecond=0).isoformat().replace("+00:00", "Z")
    updated = replace(req, state=to_state, updated=now_iso)
    body = req.notes
    if note:
        body = (body or "").rstrip() + (
            f"\n\n## {now_iso}: → {to_state}\n{note.strip()}\n"
        )
    target = request_path(core, request_id, env)
    target.write_text(_render_request_md(updated, body=body), encoding="utf-8")
    updated.path = target
    root = lab_info_root(env)
    _git_init_if_needed(root)
    _git_commit_all(root,
        f"core {core}: request {request_id} -> {to_state}")
    return updated


def update_booking_slot(
    *,
    core: str,
    request_id: str,
    booked_slot: BookingSlot,
    env: dict[str, str] | None = None,
) -> RequestSummary:
    """Replace a request's calendar window (reschedule). Doesn't
    change state; caller can chain transition_request."""
    req = get_request(core, request_id, env=env)
    if req is None:
        raise RequestError(f"request not found: {core}/{request_id}")
    now_iso = _dt.datetime.now(_dt.timezone.utc).replace(microsecond=0).isoformat().replace("+00:00", "Z")
    updated = replace(req, booked_slot=booked_slot, updated=now_iso)
    target = request_path(core, request_id, env)
    target.write_text(_render_request_md(updated, body=req.notes),
                       encoding="utf-8")
    updated.path = target
    root = lab_info_root(env)
    _git_init_if_needed(root)
    _git_commit_all(root,
        f"core {core}: request {request_id} slot updated "
        f"({booked_slot.start} -> {booked_slot.end})")
    return updated


