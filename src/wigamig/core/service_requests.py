"""
Purpose: Per-core service-request records — the booking-side analogue
         of SEAs for research projects.
Author: Mike Hallett (with Claude Code)
Date: 2026-05-22
Input: ``<lab_info>/cores/<core>/lab-mgmt/requests/<request_id>.md``
       (frontmatter parsed via core.frontmatter).
Output: ``RequestSummary`` per booking + state-machine helpers for
        Phase 3 lifecycle.

Distinct from ``core.requests`` (which models project-join requests
at the lab level). This module is the **service booking** record for
a core's catalog — created when a member books a slot, advances
through a state machine that mirrors SEAs:

    requested ──schedule──▶ scheduled ──start──▶ in_progress
                              │                       │
                              └──cancel──┐            └──complete──▶ completed
                                         ▼
                                     cancelled

Storage layout:

  <lab_info>/cores/<core>/lab-mgmt/requests/<request_id>.md

``request_id`` is generated by :func:`new_request_id` as
``YYYY-MM-DD-<requester>-<service>-NNN`` — sortable + human-readable
+ filename-safe. Schema in :class:`RequestSummary`; cross-reference
docs/cores_plan.md §5b.

Mutations commit-audit via the existing lab_info git infrastructure
(same pattern as create_service in Phase 2c).
"""

from __future__ import annotations

import datetime as _dt
import re as _re
from dataclasses import dataclass, field, replace
from pathlib import Path
from typing import Any

from .frontmatter import parse_file
from .registrar import (
    LabNotFound as _LabNotFound,
    _git_commit_all,
    _git_init_if_needed,
    lab_info_root,
    read_registry as _read_registry,
)


REQUESTS_SUBDIR = "lab-mgmt/requests"

STATE_REQUESTED   = "requested"
STATE_SCHEDULED   = "scheduled"
STATE_IN_PROGRESS = "in_progress"
STATE_COMPLETED   = "completed"
STATE_CANCELLED   = "cancelled"

VALID_STATES = (
    STATE_REQUESTED, STATE_SCHEDULED, STATE_IN_PROGRESS,
    STATE_COMPLETED, STATE_CANCELLED,
)

ALLOWED_TRANSITIONS: dict[str, set[str]] = {
    STATE_REQUESTED:   {STATE_SCHEDULED, STATE_CANCELLED},
    STATE_SCHEDULED:   {STATE_IN_PROGRESS, STATE_CANCELLED},
    STATE_IN_PROGRESS: {STATE_COMPLETED, STATE_CANCELLED},
    STATE_COMPLETED:   set(),
    STATE_CANCELLED:   set(),
}

TERMINAL_STATES = {STATE_COMPLETED, STATE_CANCELLED}


class RequestError(RuntimeError):
    """Service-request mutation failed (bad transition, missing file, …)."""


@dataclass
class BookingSlot:
    """The calendar window a request occupies."""

    start: str = ""        # ISO8601 with tz, e.g. "2026-05-23T10:00-04:00"
    end: str = ""          # ISO8601 with tz
    calendar_event_id: str = ""   # opaque Google Calendar id, when wired


@dataclass
class FeeSnapshot:
    """Fees frozen at booking time. Mirrors services.quote_fee output."""

    tier: str = ""
    unit: str = "per_run"
    base: float = 0.0
    modifiers_applied: list[dict[str, Any]] = field(default_factory=list)
    total: float = 0.0


@dataclass
class RequestSummary:
    request_id: str
    service: str
    core: str
    requester: str                        # '@handle'
    requester_lab: str                    # for billing + data delivery
    state: str = STATE_REQUESTED
    booked_slot: BookingSlot = field(default_factory=BookingSlot)
    training_verified: dict[str, str] = field(default_factory=dict)
    prerequisites_attested: list[dict[str, bool]] = field(default_factory=list)
    fee_at_booking: FeeSnapshot = field(default_factory=FeeSnapshot)
    notes: str = ""
    job_id: str = ""                      # data-delivery dir name
    created: str = ""
    updated: str = ""
    path: Path | None = None


# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------

def requests_dir(core: str, env: dict[str, str] | None = None) -> Path:
    """``<lab_info>/cores/<core>/lab-mgmt/requests/``."""
    return lab_info_root(env) / "cores" / core / "lab-mgmt" / "requests"


def request_path(core: str, request_id: str,
                  env: dict[str, str] | None = None) -> Path:
    return requests_dir(core, env) / f"{request_id}.md"


# ---------------------------------------------------------------------------
# Id generator
# ---------------------------------------------------------------------------

_SLUG_BAD = _re.compile(r"[^a-z0-9]+")


def _slugify(s: str) -> str:
    return _SLUG_BAD.sub("_", (s or "").strip().lower()).strip("_") or "x"


def new_request_id(
    *,
    core: str,
    requester: str,
    service: str,
    today: _dt.date | None = None,
    env: dict[str, str] | None = None,
) -> str:
    """``YYYY-MM-DD-<requester>-<service>-NNN``."""
    today = today or _dt.date.today()
    base = f"{today.isoformat()}-{_slugify(requester)}-{_slugify(service)}"
    rdir = requests_dir(core, env)
    existing = set()
    if rdir.is_dir():
        existing = {p.stem for p in rdir.glob(f"{base}-*.md")}
    for n in range(1, 1000):
        candidate = f"{base}-{n:03d}"
        if candidate not in existing:
            return candidate
    raise RequestError(
        f"more than 1000 requests for {requester}/{service} on {today}; "
        "investigate before bumping the suffix width."
    )


# ---------------------------------------------------------------------------
# Render / parse
# ---------------------------------------------------------------------------

def _to_dict(req: RequestSummary) -> dict:
    return {
        "request_id": req.request_id,
        "service": req.service,
        "core": req.core,
        "requester": req.requester,
        "requester_lab": req.requester_lab,
        "state": req.state,
        "booked_slot": {
            "start": req.booked_slot.start,
            "end": req.booked_slot.end,
            "calendar_event_id": req.booked_slot.calendar_event_id,
        },
        "training_verified": dict(req.training_verified or {}),
        "prerequisites_attested": list(req.prerequisites_attested or []),
        "fee_at_booking": {
            "tier": req.fee_at_booking.tier,
            "unit": req.fee_at_booking.unit,
            "base": req.fee_at_booking.base,
            "modifiers_applied": list(req.fee_at_booking.modifiers_applied or []),
            "total": req.fee_at_booking.total,
        },
        "job_id": req.job_id,
        "created": req.created,
        "updated": req.updated,
    }


def _render_request_md(req: RequestSummary, body: str = "") -> str:
    import yaml as _y
    meta = _to_dict(req)
    yaml_text = _y.safe_dump(meta, sort_keys=False, allow_unicode=True).rstrip()
    body = body.strip() or f"# Request {req.request_id}"
    return f"---\n{yaml_text}\n---\n\n{body}\n"


def _parse_request(path: Path) -> RequestSummary | None:
    try:
        parsed = parse_file(path)
    except Exception:
        return None
    meta = parsed.meta or {}
    if not meta.get("request_id"):
        return None
    slot_raw = meta.get("booked_slot") or {}
    fee_raw = meta.get("fee_at_booking") or {}
    return RequestSummary(
        request_id=str(meta["request_id"]),
        service=str(meta.get("service") or ""),
        core=str(meta.get("core") or ""),
        requester=str(meta.get("requester") or ""),
        requester_lab=str(meta.get("requester_lab") or ""),
        state=str(meta.get("state") or STATE_REQUESTED),
        booked_slot=BookingSlot(
            start=str(slot_raw.get("start") or ""),
            end=str(slot_raw.get("end") or ""),
            calendar_event_id=str(slot_raw.get("calendar_event_id") or ""),
        ),
        training_verified=dict(meta.get("training_verified") or {}),
        prerequisites_attested=list(meta.get("prerequisites_attested") or []),
        fee_at_booking=FeeSnapshot(
            tier=str(fee_raw.get("tier") or ""),
            unit=str(fee_raw.get("unit") or "per_run"),
            base=float(fee_raw.get("base") or 0.0),
            modifiers_applied=list(fee_raw.get("modifiers_applied") or []),
            total=float(fee_raw.get("total") or 0.0),
        ),
        notes=(parsed.body or "").strip(),
        job_id=str(meta.get("job_id") or ""),
        created=str(meta.get("created") or ""),
        updated=str(meta.get("updated") or ""),
        path=path,
    )


