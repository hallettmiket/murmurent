# Reader
# ---------------------------------------------------------------------------

def iter_requests(
    core: str,
    *,
    state: str | None = None,
    requester: str | None = None,
    requester_lab: str | None = None,
    include_terminal: bool = True,
    env: dict[str, str] | None = None,
) -> list[RequestSummary]:
    """List requests for ``core``. Filters compose (state, requester,
    requester_lab). ``include_terminal=False`` hides completed +
    cancelled (useful for an active-only inbox view).
    Sort: newest first by ``created``."""
    rdir = requests_dir(core, env)
    if not rdir.is_dir():
        return []
    out: list[RequestSummary] = []
    requester_norm = (requester or "").lstrip("@").lower() or None
    for entry in sorted(rdir.iterdir()):
        if not entry.is_file() or entry.suffix != ".md":
            continue
        req = _parse_request(entry)
        if req is None:
            continue
        if state and req.state != state:
            continue
        if not include_terminal and req.state in TERMINAL_STATES:
            continue
        if requester_norm and req.requester.lstrip("@").lower() != requester_norm:
            continue
        if requester_lab and req.requester_lab != requester_lab:
            continue
        out.append(req)
    out.sort(key=lambda r: r.created, reverse=True)
    return out


def get_request(
    core: str,
    request_id: str,
    *,
    env: dict[str, str] | None = None,
) -> RequestSummary | None:
    path = request_path(core, request_id, env)
    if not path.is_file():
        return None
    return _parse_request(path)


